              .h4 Publicidad
                br
                br
                p Según Philip Kotler la publicidad es “una comunicación no personal y onerosa de promoción de ideas, bienes o servicios, que lleva a cabo un patrocinador identificado”.
                br
                p Para la AMA (American Marketing Asociation) la publicidad es “la colocación de avisos y mensajes persuasivos, en tiempo o espacio, comprado en cualesquiera de los medios de comunicación por empresas lucrativas, organizaciones no lucrativas, agencias del estado y los individuos que intentan informar y/o persuadir a los miembros de un mercado meta en particular o a audiencias acerca de sus productos, servicios, organizaciones o ideas”. Citado por Puon (2013).
                br
                p De acuerdo con ambas definiciones, la publicidad es una técnica de comunicación que se aplica en medios convencionales con objetivos comerciales; es decir, es pagada y busca el retorno de ese pago a través de la respuesta del mercado objetivo al que va dirigido el mensaje. La publicidad es programada en tiempo en los medios electrónicos como la televisión, la radio y actualmente en plataformas audiovisuales como YouTube y Facebook; así como en espacio en medios impresos como revistas y prensa o en sitios web a través de compra de pauta en Google AdWords.
          
     
