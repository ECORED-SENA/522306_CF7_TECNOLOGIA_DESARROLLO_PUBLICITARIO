              .h4 Propaganda
              br
            
              p Esta técnica suele confundirse con la publicidad; sin embargo, existe un abismo de diferencia entre ellas, si bien ambas pueden coincidir en el uso de algunos medios, su fin es muy diferente. La AMA (American Marketing Asociation) la define como “las ideas, información u otro material difundido comúnmente a través de los medios (periódicos, páginas amarillas, radio, televisión, etc.) en un esfuerzo por ganar a personas para una doctrina o punto de vista”. Citado por Thompson (2006). 
              br
              p Con la llegada de las redes sociales se ha podido utilizar esta estrategia para propósitos comerciales y políticos más que nunca. Cuando se postea algo, ya sea de índole personal o comercial sin acudir a la publicidad paga en las redes sociales, se está haciendo propaganda; para el caso los likes que recibe dicho post o las veces que es compartido se convierte la propagación de una información sin haber pagado en los medios, solo por la convicción personal de quien los hizo, ya sea por gusto, por coincidencia ideológica o por apoyo a la causa. La propaganda es por lo tanto la divulgación sin ánimo de lucro de una idea, del concepto que se tenga de algo o de alguien; de ahí que su uso sea más frecuente en el escenario político, sin embargo, con estrategias bien estructuradas puede llegar a ser muy eficaz también en lo comercial.


